package com.leantaas.javafxbeans;


public class TestcaseProperty {
	public TestcaseProperty() {
		
	}

	String internalid;

	String name;

	String node_order;

	String externalid;

	String version;

	String summary;

	String preconditions;

	String execution_type;

	String importance;

	StepsProperty steps;

	public String getInternalid() {
		return internalid;
	}

	public void setInternalid(String internalid) {
		this.internalid = internalid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNodeOrder() {
		return node_order;
	}

	public void setNodeOrder(String node_order) {
		this.node_order = node_order;
	}

	public String getExternalid() {
		return externalid;
	}

	public void setExternalid(String externalid) {
		this.externalid = externalid;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public String getPreconditions() {
		return preconditions;
	}

	public void setPreconditions(String preconditions) {
		this.preconditions = preconditions;
	}

	public String getExecutionType() {
		return execution_type;
	}

	public void setExecutionType(String execution_type) {
		this.execution_type = execution_type;
	}

	public String getImportance() {
		return importance;
	}

	public void setImportance(String importance) {
		this.importance = importance;
	}

	public StepsProperty getSteps() {
		return steps;
	}

	public void setSteps(StepsProperty steps) {
		this.steps = steps;
	}

}
